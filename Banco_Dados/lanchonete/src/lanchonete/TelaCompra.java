    private void CadastrarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarCompraActionPerformed
        if (Campocpfcompra.getText().equals("") || Campodatacompra.getText().equals("") || Campofunccompra.getText().equals("") || Campototalcompra.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha os campos corretamente");
        } else {
            try{
        Compras c = new Compras(Campocpfcompra.getText(), Campodatacompra.getText(), Double.parseDouble(Campototalcompra.getText()), Campofunccompra.getText());
        AcessoBD_lanchonete.salvar_compra(c);
        Campocpfcompra.setText("");
        Campodatacompra.setText("");
        Campofunccompra.setText("");
        Campototalcompra.setText("");
            JOptionPane.showMessageDialog(this, "Compra cadastrada!");

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Compra n√£o cadastrada!");
            }
        }
    }//GEN-LAST:event_CadastrarCompraActionPerformed

    private void PesquisarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarCompraActionPerformed
        if (Campocpfcompra.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha o campo cpf");
        } else if(AcessoBD_lanchonete.pesquisa_compra("compra", Campocpfcompra.getText()) != true){
            JOptionPane.showMessageDialog(this, "Compra n√£o cadastrada");
        }
        else if(AcessoBD_lanchonete.pesquisa_compra("compra", Campocpfcompra.getText()) == true){
            AcessoBD_lanchonete.pesquisa_compra("compra", Campocpfcompra.getText());
        }
    }//GEN-LAST:event_PesquisarCompraActionPerformed

    private void ExcluirCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirCompraActionPerformed
        if (Campocpfcompra.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Preencha o campo cpf corretamente");
        }
        else {
            try{
            AcessoBD_lanchonete.deleta_compra(Campocpfcompra.getText());
            JOptionPane.showMessageDialog(this, "Compra deletada");
        }
            catch(Exception e){
                JOptionPane.showMessageDialog(this, "Compra n√£o deletada");
            }
        }
    }//GEN-LAST:event_ExcluirCompraActionPerformed

